    private void imprimirMonstruo(JLabel etiqueta, Monstruo[] monstruos, byte i) {
        if (monstruos[i].isVisible()) {
            String posicion;
            if(monstruos[i].isEnPosicionAtaque()){
                posicion = "En ataque";
            } 
            else{
                posicion = "En defensa";
            }
            String textoFormateado = "<html><div style='text-align:center;'>" + monstruos[i].getNombre() + "<br>Nv: " + monstruos[i].getNivel() + "<br>ATK: " + monstruos[i].getAtaque() + "<br>DEF: " + monstruos[i].getDefensa() + "<br>" + posicion + "</div></html>";
            etiqueta.setText(textoFormateado);
        } 
        else{
            etiqueta.setText("<html>Carta no visible</html>");
        }
    }
    private void imprimirMagiasYTrampas(JLabel etiqueta, Carta[] magicasOTrampas, byte i){
        if (magicasOTrampas[i] instanceof Magia) {
            etiqueta.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 153, 0)));
        } 
        else{
            etiqueta.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(102, 0, 102)));
        }
        if (magicasOTrampas[i].isVisible()) {
            String nombre = magicasOTrampas[i].getNombre();
            String descripcion = magicasOTrampas[i].getCuadroDeTexto();
            String textoFormateado = "<html><div style='text-align:center;'>" +nombre + "<br>" + descripcion + "</div></html>";
            etiqueta.setText(textoFormateado);
        } 
        else{
            etiqueta.setText("<html>Carta no visible</html>");
        }
    }
